rom 和 ram 的差別
rom 就是像隨身碟之類的，資料存好後斷電也不會不見的，早期寫死在硬體就不能修改了，後來有出用電位來寫入資料的，就可以修改了
ram 通電才能存資料，一斷電資料就全部不見了

CMOS與BIOS
前面記憶體的地方我們有提過CMOS與BIOS的功能，在這裡我們再來強調一下： CMOS主要的功能為記錄主機板上面的重要參數， 包括系統時間、CPU電壓與頻率、各項設備的I/O位址與IRQ等，
由於這些資料的記錄要花費電力，因此主機板上面才有電池。 BIOS為寫入到主機板上某一塊 flash 或 EEPROM 的程式，他可以在開機的時候執行，以載入CMOS當中的參數，
 並嘗試呼叫儲存裝置中的開機程式，進一步進入作業系統當中。BIOS程式也可以修改CMOS中的資料， 每種主機板呼叫BIOS設定程式的按鍵都不同，一般桌上型電腦常見的是使用[del]按鍵進入BIOS設定畫面。

