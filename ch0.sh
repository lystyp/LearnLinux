DRAM與SRAM
除了主記憶體之外，事實上整部個人電腦當中還有許許多多的記憶體存在喔！最為我們所知的就是CPU內的第二層快取記憶體。 我們現在知道CPU的資料都是由主記憶體提供，但CPU到主記憶體之間還是得要透過記憶體控制器啊！
 如果某些很常用的程式或資料可以放置到CPU內部的話，那麼CPU資料的讀取就不需要跑到主記憶體重新讀取了！ 這對於效能來說不就可以大大的提升了？這就是第二層快取的設計概念。

 CPU - 暫存器 - 第二層快取記憶體 - 外接記憶體 - 外接硬碟

