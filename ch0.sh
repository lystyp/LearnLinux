主機板上面溝通各部元件的晶片組設計優劣，就會影響效能不少喔！早期的晶片組通常分為兩個橋接器來控制各元件的溝通， 分別是：
(1)北橋：負責連結速度較快的CPU、主記憶體與顯示卡界面等元件；
(2)南橋：負責連接速度較慢的裝置介面， 包括硬碟、USB、網路卡等等。
不過由於北橋最重要的就是 CPU 與主記憶體之間的橋接，因此目前的主流架構中， 大多將北橋記憶體控制器整合到 CPU 封裝當中了。

