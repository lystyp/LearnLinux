CPU架構
CISC
複雜指令集(Complex Instruction Set Computer, CISC)
與RISC不同的，CISC在微指令集的每個小指令可以執行一些較低階的硬體操作，指令數目多而且複雜， 每條指令的長度並不相同。
因為指令執行較為複雜所以每條指令花費的時間較長， 但每條個別指令可以處理的工作較為豐富。
常見的CISC微指令集CPU主要有AMD、Intel、VIA等的x86架構的CPU（一般電腦）。

由於AMD、Intel、VIA所開發出來的x86架構CPU被大量使用於個人電腦(Personal computer)用途上面， 因此，個人電腦常被稱為x86架構的電腦！那為何稱為x86架構(註8)呢？ 
這是因為最早的那顆Intel發展出來的CPU代號稱為8086

在2003年以前由Intel所開發的x86架構CPU由8位元升級到16、32位元，後來AMD依此架構修改新一代的CPU為64位元， 為了區別兩者的差異，因此64位元的個人電腦CPU又被統稱為x86_64的架構喔！

RISC
精簡指令集 (Reduced Instruction Set Computer, RISC)
所以這個的每個指令長度短且一樣
常見的 RISC 微指令集 CPU 主要例如甲骨文 (Oracle) 公司的 SPARC 系列、 IBM 公司的 Power Architecture (包括 PowerPC) 系列
、與安謀公司 (ARM Holdings) 的 ARM CPU (這是手機的？)系列等。

這些指令就是用組語寫的吧？
每個CPU自己都有自己的指令組，但都大同小異啦

